#!/usr/bin/env python3
"""
Figure 3: Baseline vs full model comparison for the Line 1 DES manuscript.

Purpose
-------
Creates publication-ready plots comparing trivial baselines against the audited
full model under a selected validation protocol.

Recommended main-text setting
-----------------------------
protocol = pair_ratio_group
This corresponds to Figure 2 Protocol B: leakage-corrected pair+ratio grouping.

Required inputs
---------------
1) --baseline-metrics: baseline_outputs/baseline_metrics_summary.csv
2) --full-model-csv: figure2_outputs/Figure2_validation_degradation_data.csv

Outputs
-------
- Figure3_baseline_vs_full_grouped_bar.png/pdf
- Figure3_delta_full_minus_temperature.png/pdf
- Figure3_baseline_vs_full_data.csv
- Figure3_caption.txt
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Dict, Tuple

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

PROPERTY_ORDER = [
    "density",
    "refractive_index",
    "surface_tension",
    "conductivity",
    "viscosity",
]

PROPERTY_LABELS = {
    "density": "Density",
    "refractive_index": "Refractive index",
    "surface_tension": "Surface tension",
    "conductivity": "Conductivity",
    "viscosity": "Viscosity",
}

FEATURE_LABELS = {
    "dummy_mean": "Mean baseline",
    "temperature_only": "Temperature-only",
    "ratio_only": "Ratio-only",
    "temperature_plus_ratio": "Temperature + ratio",
    "temperature_ratio_identity": "Temp + ratio + identity",
    "component_identity_only": "Component identity",
    "full_model": "Full model",
}

# Mapping between baseline protocol names and Figure 2 full-model protocol names
PROTOCOL_TO_FULL = {
    "pair_ratio_group": "B_leakage_corrected_pair_ratio_group",
    "pair_group": "C_strict_pair_group",
    "leave_hba_out": "D_leave_HBA_out",
    "leave_hbd_out": "D_leave_HBD_out",
}


def normalize_property_name(x: str) -> str:
    x = str(x).strip()
    mapping = {
        "density_g_cm3": "density",
        "density": "density",
        "refractive_index": "refractive_index",
        "surface_tension_mn_m": "surface_tension",
        "surface_tension": "surface_tension",
        "conductivity_ms_cm": "conductivity",
        "conductivity": "conductivity",
        "viscosity_mpa_s": "viscosity",
        "viscosity": "viscosity",
    }
    return mapping.get(x, x)


def read_csv_checked(path: Path, label: str) -> pd.DataFrame:
    if not path.exists():
        raise FileNotFoundError(f"{label} not found: {path}")
    if path.stat().st_size <= 2:
        raise ValueError(
            f"{label} appears empty: {path}\n"
            "For Figure 3, do not use the empty Table4_baseline_comparison.csv generated by the table builder.\n"
            "Use baseline_outputs/baseline_metrics_summary.csv instead."
        )
    df = pd.read_csv(path)
    if df.empty:
        raise ValueError(f"{label} is empty after reading: {path}")
    return df


def load_baselines(path: Path, protocol: str) -> pd.DataFrame:
    df = read_csv_checked(path, "Baseline metrics CSV")
    required = {"property", "protocol", "feature_set", "r2", "mae", "rmse", "n_eval"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(
            f"Baseline metrics CSV is missing required columns: {sorted(missing)}\n"
            f"Found columns: {list(df.columns)}"
        )

    df = df.copy()
    df["property"] = df["property"].map(normalize_property_name)
    df = df[df["protocol"] == protocol].copy()
    if df.empty:
        available = sorted(pd.read_csv(path, usecols=["protocol"])["protocol"].dropna().unique())
        raise ValueError(f"No baseline rows found for protocol={protocol}. Available protocols: {available}")

    keep_features = ["dummy_mean", "temperature_only", "ratio_only", "temperature_plus_ratio"]
    df = df[df["feature_set"].isin(keep_features)].copy()
    df["model_label"] = df["feature_set"].map(FEATURE_LABELS)
    df["source"] = "baseline"
    df = df.rename(columns={"r2": "r2_mean", "mae": "mae_mean", "rmse": "rmse_mean"})
    df["r2_std"] = np.nan
    return df[["property", "model_label", "feature_set", "r2_mean", "r2_std", "mae_mean", "rmse_mean", "n_eval", "source"]]


def load_full_model(path: Path, baseline_protocol: str) -> pd.DataFrame:
    df = read_csv_checked(path, "Full-model Figure 2 CSV")
    required = {"property", "protocol", "r2_mean", "r2_std", "mae_mean", "rmse_mean"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(
            f"Full-model CSV is missing required columns: {sorted(missing)}\n"
            f"Found columns: {list(df.columns)}"
        )

    full_protocol = PROTOCOL_TO_FULL.get(baseline_protocol)
    if full_protocol is None:
        raise ValueError(
            f"Unsupported protocol for full-model mapping: {baseline_protocol}. "
            f"Use one of {sorted(PROTOCOL_TO_FULL)}"
        )

    df = df.copy()
    df["property"] = df["property"].map(normalize_property_name)
    df = df[df["protocol"] == full_protocol].copy()
    if df.empty:
        available = sorted(pd.read_csv(path, usecols=["protocol"])["protocol"].dropna().unique())
        raise ValueError(f"No full-model rows found for protocol={full_protocol}. Available protocols: {available}")

    if "n_test_total" in df.columns:
        n_col = "n_test_total"
    elif "n_eval" in df.columns:
        n_col = "n_eval"
    else:
        df["n_eval"] = np.nan
        n_col = "n_eval"

    df["model_label"] = "Full model"
    df["feature_set"] = "full_model"
    df["source"] = "full_model"
    return df[["property", "model_label", "feature_set", "r2_mean", "r2_std", "mae_mean", "rmse_mean", n_col, "source"]].rename(columns={n_col: "n_eval"})


def assemble_data(baseline_csv: Path, full_csv: Path, protocol: str) -> pd.DataFrame:
    base = load_baselines(baseline_csv, protocol)
    full = load_full_model(full_csv, protocol)
    data = pd.concat([base, full], ignore_index=True)
    data = data[data["property"].isin(PROPERTY_ORDER)].copy()
    data["property_label"] = data["property"].map(PROPERTY_LABELS)
    data["property_order"] = data["property"].map({p: i for i, p in enumerate(PROPERTY_ORDER)})
    model_order = {
        "Mean baseline": 0,
        "Temperature-only": 1,
        "Ratio-only": 2,
        "Temperature + ratio": 3,
        "Full model": 4,
    }
    data["model_order"] = data["model_label"].map(model_order)
    data = data.sort_values(["property_order", "model_order"]).reset_index(drop=True)
    return data


def plot_grouped_bar(data: pd.DataFrame, outdir: Path) -> None:
    properties = [p for p in PROPERTY_ORDER if p in set(data["property"])]
    model_labels = ["Mean baseline", "Temperature-only", "Ratio-only", "Temperature + ratio", "Full model"]
    x = np.arange(len(properties))
    width = 0.15

    fig, ax = plt.subplots(figsize=(11, 5.5))
    for i, model in enumerate(model_labels):
        vals = []
        errs = []
        for p in properties:
            row = data[(data["property"] == p) & (data["model_label"] == model)]
            if row.empty:
                vals.append(np.nan)
                errs.append(0.0)
            else:
                vals.append(float(row.iloc[0]["r2_mean"]))
                err = row.iloc[0]["r2_std"]
                errs.append(0.0 if pd.isna(err) else float(err))
        offset = (i - (len(model_labels) - 1) / 2) * width
        ax.bar(x + offset, vals, width, label=model, yerr=errs, capsize=2)

    ax.axhline(0, color="black", linewidth=0.8, linestyle="--")
    ax.set_xticks(x)
    ax.set_xticklabels([PROPERTY_LABELS[p] for p in properties], rotation=25, ha="right")
    ax.set_ylabel("Cross-validated R²")
    ax.set_xlabel("")
    ax.set_title("Baseline comparison against the full leakage-safe model")
    ax.legend(frameon=False, ncol=3, fontsize=9)
    ax.grid(axis="y", alpha=0.25)
    fig.tight_layout()
    fig.savefig(outdir / "Figure3_baseline_vs_full_grouped_bar.png", dpi=300)
    fig.savefig(outdir / "Figure3_baseline_vs_full_grouped_bar.pdf")
    plt.close(fig)


def plot_delta_vs_temperature(data: pd.DataFrame, outdir: Path) -> pd.DataFrame:
    rows = []
    for p in PROPERTY_ORDER:
        temp = data[(data["property"] == p) & (data["model_label"] == "Temperature-only")]
        full = data[(data["property"] == p) & (data["model_label"] == "Full model")]
        dummy = data[(data["property"] == p) & (data["model_label"] == "Mean baseline")]
        if temp.empty or full.empty:
            continue
        rows.append({
            "property": p,
            "property_label": PROPERTY_LABELS[p],
            "full_r2": float(full.iloc[0]["r2_mean"]),
            "temperature_r2": float(temp.iloc[0]["r2_mean"]),
            "dummy_r2": float(dummy.iloc[0]["r2_mean"]) if not dummy.empty else np.nan,
            "delta_r2_full_minus_temperature": float(full.iloc[0]["r2_mean"] - temp.iloc[0]["r2_mean"]),
            "delta_r2_full_minus_dummy": float(full.iloc[0]["r2_mean"] - dummy.iloc[0]["r2_mean"]) if not dummy.empty else np.nan,
        })
    delta = pd.DataFrame(rows)

    fig, ax = plt.subplots(figsize=(8.5, 4.8))
    x = np.arange(len(delta))
    ax.bar(x, delta["delta_r2_full_minus_temperature"].values)
    ax.axhline(0, color="black", linewidth=0.8, linestyle="--")
    ax.set_xticks(x)
    ax.set_xticklabels(delta["property_label"].values, rotation=25, ha="right")
    ax.set_ylabel("ΔR² (Full model − Temperature-only)")
    ax.set_xlabel("")
    ax.set_title("Added value of the full feature model beyond temperature")
    ax.grid(axis="y", alpha=0.25)
    fig.tight_layout()
    fig.savefig(outdir / "Figure3_delta_full_minus_temperature.png", dpi=300)
    fig.savefig(outdir / "Figure3_delta_full_minus_temperature.pdf")
    plt.close(fig)
    return delta


def write_caption(outdir: Path, protocol: str) -> None:
    protocol_text = {
        "pair_ratio_group": "leakage-corrected HBA–HBD–ratio grouping",
        "pair_group": "strict HBA–HBD pair grouping",
        "leave_hba_out": "leave-HBA-out extrapolative validation",
        "leave_hbd_out": "leave-HBD-out extrapolative validation",
    }.get(protocol, protocol)

    caption = (
        "Figure 3. Baseline comparison for DES property prediction under "
        f"{protocol_text}. The full leakage-safe ExtraTrees model is compared with "
        "trivial baselines using only the training-set mean, temperature, molar ratio, or "
        "temperature plus ratio. The grouped bar plot reports cross-validated R², while "
        "the ΔR² panel quantifies the added value of the full feature representation over "
        "the temperature-only baseline. Positive ΔR² indicates that chemistry-informed "
        "features contribute predictive information beyond temperature alone."
    )
    (outdir / "Figure3_caption.txt").write_text(caption, encoding="utf-8")


def write_readme(outdir: Path, data: pd.DataFrame, delta: pd.DataFrame) -> None:
    lines = [
        "# Figure 3 generation report",
        "",
        f"Rows in assembled plot data: {len(data)}",
        "",
        "## Files created",
        "- Figure3_baseline_vs_full_grouped_bar.png/pdf",
        "- Figure3_delta_full_minus_temperature.png/pdf",
        "- Figure3_baseline_vs_full_data.csv",
        "- Figure3_delta_data.csv",
        "- Figure3_caption.txt",
        "",
        "## Quick interpretation",
        "Use the delta table to decide where the full model adds value beyond temperature.",
        "Positive delta supports non-trivial structure/property learning.",
    ]
    (outdir / "Figure3_README.md").write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description="Build Figure 3: baselines vs full model.")
    parser.add_argument("--baseline-metrics", required=True, help="Path to baseline_outputs/baseline_metrics_summary.csv")
    parser.add_argument("--full-model-csv", required=True, help="Path to figure2_outputs/Figure2_validation_degradation_data.csv")
    parser.add_argument(
        "--protocol",
        default="pair_ratio_group",
        choices=["pair_ratio_group", "pair_group", "leave_hba_out", "leave_hbd_out"],
        help="Validation protocol to plot. Recommended: pair_ratio_group for main text.",
    )
    parser.add_argument("--outdir", default="figure3_outputs", help="Output directory")
    args = parser.parse_args()

    outdir = Path(args.outdir)
    outdir.mkdir(parents=True, exist_ok=True)

    data = assemble_data(Path(args.baseline_metrics), Path(args.full_model_csv), args.protocol)
    data.to_csv(outdir / "Figure3_baseline_vs_full_data.csv", index=False)

    plot_grouped_bar(data, outdir)
    delta = plot_delta_vs_temperature(data, outdir)
    delta.to_csv(outdir / "Figure3_delta_data.csv", index=False)

    write_caption(outdir, args.protocol)
    write_readme(outdir, data, delta)

    print(f"Done. Outputs written to: {outdir}")
    print("Created:")
    for name in [
        "Figure3_baseline_vs_full_grouped_bar.png/pdf",
        "Figure3_delta_full_minus_temperature.png/pdf",
        "Figure3_baseline_vs_full_data.csv",
        "Figure3_delta_data.csv",
        "Figure3_caption.txt",
    ]:
        print(f" - {name}")


if __name__ == "__main__":
    main()
