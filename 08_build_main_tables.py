#!/usr/bin/env python3
"""
Build manuscript-ready tables for the audited DES ML manuscript.

Main manuscript tables generated by this workflow:
  Table1_dataset_overview.csv       -> computed directly from the frozen GOLD dataset
  Table2_validation_protocols.csv   -> protocol definitions
  Table3_validation_performance.csv -> computed from Figure2_validation_degradation_data.csv
  Table4_baseline_comparison.csv    -> computed from baseline_metrics_summary.csv + Figure2 data

Supporting/SI tables are also generated when inputs are available:
  TableS1_feature_ablation.csv
  TableS2_interpretability_top_features.csv
"""
from __future__ import annotations

