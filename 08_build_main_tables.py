import csv
import math
import re
from pathlib import Path
from typing import Dict, List, Optional

TARGETS = {
    "density": "density_g_cm3",
    "viscosity": "viscosity_mpa_s",
    "conductivity": "conductivity_ms_cm",
    "surface_tension": "surface_tension_mn_m",
    "refractive_index": "refractive_index",
}
PROPERTY_ORDER = ["density", "refractive_index", "surface_tension", "conductivity", "viscosity"]
TARGET_TO_PROP = {v: k for k, v in TARGETS.items()}
PRETTY = {
    "density": "Density",
    "refractive_index": "Refractive index",
    "surface_tension": "Surface tension",
    "conductivity": "Electrical conductivity",
    "viscosity": "Viscosity",
}
PROTOCOL_PRETTY = {
    "B_leakage_corrected_pair_ratio_group": "B: leakage-corrected pair+ratio",
    "C_strict_pair_group": "C: strict pair",
    "D_leave_HBA_out": "D1: leave-HBA-out",
    "D_leave_HBD_out": "D2: leave-HBD-out",
    "pair_ratio_group": "Pair+ratio group",
    "pair_group": "Pair group",
    "leave_hba_out": "Leave-HBA-out",
    "leave_hbd_out": "Leave-HBD-out",
    "random_kfold": "Random K-fold",
}
MODEL_PRETTY = {
    "extra_trees": "ExtraTrees",
    "ExtraTrees": "ExtraTrees",
    "ridge": "Ridge",
    "Ridge": "Ridge",
    "hist_gradient_boosting": "HistGB",
    "HistGB": "HistGB",
}
BASELINE_PRETTY = {
    "dummy_mean": "Dummy mean",
    "temperature_only": "Temperature-only",
    "ratio_only": "Ratio-only",
    "temperature_plus_ratio": "Temperature+ratio",
    "component_identity_only": "Component identity",
    "temperature_ratio_identity": "Temperature+ratio+identity",
    "full_model": "Full model",
}


def read_csv(path: Path) -> List[Dict[str, str]]:
    if not path.exists():
        return []
    with path.open("r", newline="", encoding="utf-8-sig") as f:
        return list(csv.DictReader(f))


def write_csv(path: Path, rows: List[Dict[str, object]], fieldnames: Optional[List[str]] = None) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if fieldnames is None:
        fieldnames = []
        for r in rows:
            for k in r.keys():
                if k not in fieldnames:
                    fieldnames.append(k)
    with path.open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writeheader()
        for r in rows:
            w.writerow({k: r.get(k, "") for k in fieldnames})


def to_float(x: object) -> Optional[float]:
    if x is None:
        return None
    s = str(x).strip()
    if s == "" or s.lower() in {"nan", "none", "null"}:
        return None
    try:
        v = float(s)
    except Exception:
        return None
    return v if math.isfinite(v) else None


def fmt(x: object, nd: int = 3) -> str:
    v = to_float(x)
    return "" if v is None else f"{v:.{nd}f}"


def fmt_pm(mean: object, std: object, nd: int = 3) -> str:
    m = to_float(mean)
    s = to_float(std)
    if m is None:
        return ""
    if s is None:
        return f"{m:.{nd}f}"
    return f"{m:.{nd}f} ± {s:.{nd}f}"


def prop_from_row(row: Dict[str, str]) -> str:
    if row.get("property"):
        return row["property"]
    if row.get("target_col") in TARGET_TO_PROP:
        return TARGET_TO_PROP[row["target_col"]]
    return row.get("target_col", "")


def parse_hbd_hba_ratio(x: object) -> Optional[float]:
    """Parse HBD/HBA molar ratio using the same logic used for the final Table 1."""
    if x is None:
        return None
    s = str(x).strip().lower().replace(" ", "")
    if s in {"unknown", "nan", "none", ""}:
        return None
    if ":" in s:
        left, right = s.split(":", 1)
        m1 = re.match(r"([0-9]*\.?[0-9]+)", left)
        m2 = re.match(r"([0-9]*\.?[0-9]+)", right)
        if m1 and m2:
            hba = float(m1.group(1))
            hbd = float(m2.group(1))
            return hbd / hba if hba != 0 else None
    m = re.match(r"([0-9]*\.?[0-9]+)", s)
    if m:
        return float(m.group(1))
    return None


def build_table1_dataset(dataset: Path, outdir: Path) -> List[Dict[str, object]]:
    rows = []
    data_rows = read_csv(dataset)
    if not data_rows:
        raise FileNotFoundError(f"Dataset missing or unreadable: {dataset}")
    for prop in PROPERTY_ORDER:
        target = TARGETS[prop]
        sub = [r for r in data_rows if to_float(r.get(target)) is not None]
        temps_k = []
        ratios = []
        hbas = set()
        hbds = set()
        for r in sub:
            tc = to_float(r.get("measurement_temperature_c"))
            if tc is not None:
                temps_k.append(tc + 273.15)
            ratio = parse_hbd_hba_ratio(r.get("molar_ratio_raw"))
            if ratio is not None:
                ratios.append(ratio)
            hba = r.get("hba_name_raw") or r.get("hba_name_resolved") or r.get("hba_name_canonical")
            hbd = r.get("hbd_name_raw") or r.get("hbd_name_resolved") or r.get("hbd_name_canonical")
            if hba:
                hbas.add(hba)
            if hbd:
                hbds.add(hbd)
        rows.append({
            "Property": PRETTY[prop],
            "N": len(sub),
            "Temperature range (K)": f"{min(temps_k):.2f}–{max(temps_k):.2f}" if temps_k else "",
            "Ratio range (HBD/HBA)": f"{min(ratios):.2f}–{max(ratios):.2f}" if ratios else "",
            "Unique HBA": len(hbas),
            "Unique HBD": len(hbds),
        })
    write_csv(outdir / "Table1_dataset_overview.csv", rows, ["Property", "N", "Temperature range (K)", "Ratio range (HBD/HBA)", "Unique HBA", "Unique HBD"])
    return rows


def build_table2_protocols(outdir: Path) -> List[Dict[str, object]]:
    rows = [
        {"Protocol": "A", "Description": "Old-like forensic reproduction of the original pipeline", "Leakage-safe": "No", "Use": "Diagnostic only"},
        {"Protocol": "B", "Description": "Leakage-corrected split grouped by HBA–HBD–ratio", "Leakage-safe": "Yes", "Use": "Main interpolation-aware audit"},
        {"Protocol": "C", "Description": "Strict HBA–HBD pair grouping", "Leakage-safe": "Yes", "Use": "Stricter group-aware validation"},
        {"Protocol": "D1", "Description": "Leave-HBA-out validation", "Leakage-safe": "Yes", "Use": "Extrapolation to unseen HBAs"},
        {"Protocol": "D2", "Description": "Leave-HBD-out validation", "Leakage-safe": "Yes", "Use": "Extrapolation to unseen HBDs"},
    ]
    write_csv(outdir / "Table2_validation_protocols.csv", rows)
    return rows


def build_table3_from_figure2(figure2_csv: Path, outdir: Path) -> List[Dict[str, object]]:
    rows_in = read_csv(figure2_csv)
    if not rows_in:
        raise FileNotFoundError(f"Figure 2 data CSV missing or unreadable: {figure2_csv}")
    rows = []
    protocol_order = {"B_leakage_corrected_pair_ratio_group": 1, "C_strict_pair_group": 2, "D_leave_HBA_out": 3, "D_leave_HBD_out": 4}
    for r in rows_in:
        prop = r.get("property", "")
        rows.append({
            "Property": PRETTY.get(prop, r.get("property_label", prop)),
            "Protocol": PROTOCOL_PRETTY.get(r.get("protocol"), r.get("protocol")),
            "Model": MODEL_PRETTY.get(r.get("model"), r.get("model", "")),
            "Feature set": r.get("feature_set", ""),
            "R² (mean ± SD)": fmt_pm(r.get("r2_mean"), r.get("r2_std")),
            "MAE (mean ± SD)": fmt_pm(r.get("mae_mean"), r.get("mae_std")),
        })
    rows.sort(key=lambda x: (PROPERTY_ORDER.index(next((p for p in PROPERTY_ORDER if PRETTY[p] == x["Property"]), "viscosity")), protocol_order.get(next((k for k, v in PROTOCOL_PRETTY.items() if v == x["Protocol"]), ""), 99)))
    write_csv(outdir / "Table3_validation_performance.csv", rows)
    return rows


def build_table4_baseline_main(baseline_csv: Path, figure2_csv: Path, outdir: Path, protocol: str = "pair_ratio_group") -> List[Dict[str, object]]:
    base_rows = read_csv(baseline_csv)
    fig_rows = read_csv(figure2_csv)
    if not base_rows:
        raise FileNotFoundError(f"Baseline metrics CSV missing or unreadable: {baseline_csv}")
    if not fig_rows:
        raise FileNotFoundError(f"Figure 2 data CSV missing or unreadable: {figure2_csv}")
    rows = []
    for prop in PROPERTY_ORDER:
        prop_base = [r for r in base_rows if r.get("property") == prop and r.get("protocol") == protocol]
        dummy = next((r for r in prop_base if r.get("feature_set") == "dummy_mean"), None)
        temp = next((r for r in prop_base if r.get("feature_set") == "temperature_only"), None)
        ratio = next((r for r in prop_base if r.get("feature_set") == "ratio_only"), None)
        full = next((r for r in fig_rows if r.get("property") == prop and r.get("protocol") == "B_leakage_corrected_pair_ratio_group"), None)
        for label, rec, is_full in [
            ("Dummy mean", dummy, False),
            ("Temperature-only", temp, False),
            ("Ratio-only", ratio, False),
            ("Full model", full, True),
        ]:
            if rec is None:
                continue
            r2 = to_float(rec.get("r2_mean") if is_full else rec.get("r2"))
            mae = to_float(rec.get("mae_mean") if is_full else rec.get("mae"))
            r2_dummy = to_float(dummy.get("r2")) if dummy else None
            r2_temp = to_float(temp.get("r2")) if temp else None
            rows.append({
                "Property": PRETTY[prop],
                "Predictor": label,
                "R²": fmt(r2),
                "MAE": fmt(mae),
                "ΔR² vs dummy": fmt((r2 - r2_dummy) if (r2 is not None and r2_dummy is not None and label != "Dummy mean") else None),
                "ΔR² vs temperature": fmt((r2 - r2_temp) if (r2 is not None and r2_temp is not None and label != "Temperature-only") else None),
            })
    write_csv(outdir / "Table4_baseline_comparison.csv", rows)
    return rows


def build_tableS1_ablation(ablation_dir: Path, outdir: Path) -> List[Dict[str, object]]:
    rows_in = read_csv(ablation_dir / "ablation_metrics_summary.csv")
    rows = []
    for r in rows_in:
        if r.get("protocol") == "pair_ratio_group" and r.get("model") in {"extra_trees", "ExtraTrees"}:
            prop = prop_from_row(r)
            rows.append({
                "Property": PRETTY.get(prop, prop),
                "Feature set": r.get("feature_set", ""),
                "Target variant": r.get("target_variant", ""),
                "R²": fmt(r.get("r2")),
                "MAE": fmt(r.get("mae")),
                "RMSE": fmt(r.get("rmse")),
            })
    write_csv(outdir / "TableS1_feature_ablation.csv", rows)
    return rows


def build_tableS2_shap(shap_dir: Path, outdir: Path, top_n: int = 8) -> List[Dict[str, object]]:
    by_run = shap_dir / "shap_by_run"
    if not by_run.exists():
        return []
    out = []
    for f in sorted(by_run.glob("*__global_shap.csv")):
        parts = f.name.replace("__global_shap.csv", "").split("__")
        if len(parts) < 4:
            continue
        target, protocol, model, feature_set = parts[:4]
        prop = TARGET_TO_PROP.get(target, target)
        rows = read_csv(f)
        if not rows:
            continue
        feature_col = "feature" if "feature" in rows[0] else list(rows[0].keys())[0]
        value_col = next((c for c in ["mean_abs_shap", "mean_abs_SHAP", "importance"] if c in rows[0]), None)
        if value_col is None:
            continue
        for rank, r in enumerate(sorted(rows, key=lambda rr: to_float(rr.get(value_col)) or -1, reverse=True)[:top_n], 1):
            out.append({
                "Property": PRETTY.get(prop, prop),
                "Protocol": PROTOCOL_PRETTY.get(protocol, protocol),
                "Model": MODEL_PRETTY.get(model, model),
                "Feature set": feature_set,
                "Rank": rank,
                "Feature": r.get(feature_col, ""),
                "Mean |SHAP|": fmt(r.get(value_col), 6),
            })
    write_csv(outdir / "TableS2_interpretability_top_features.csv", out)
    return out


def build_notes(outdir: Path) -> None:
    text = (
        "# Manuscript table notes\n\n"
        "Table 1 is computed directly from the frozen GOLD dataset using non-missing target values, "
        "measurement_temperature_c converted to Kelvin, molar_ratio_raw parsed as HBD/HBA ratio, "
        "and raw HBA/HBD names for component diversity counts.\n\n"
        "Table 3 is computed from Figure2_validation_degradation_data.csv to ensure exact consistency "
        "between the validation-performance table and Figure 2.\n\n"
        "Table 4 combines baseline_metrics_summary.csv with the full-model values from Figure 2 data, "
        "so the baseline table is consistent with Figure 3 and the validation table.\n"
    )
    (outdir / "manuscript_table_notes.md").write_text(text, encoding="utf-8")


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--dataset", required=True)
    ap.add_argument("--figure2-csv", default="figure2_outputs/Figure2_validation_degradation_data.csv")
    ap.add_argument("--baseline-csv", default="baseline_outputs/baseline_metrics_summary.csv")
    ap.add_argument("--ablation-dir", default="ablation_outputs")
    ap.add_argument("--shap-dir", default="shap_outputs_full")
    ap.add_argument("--outdir", default="manuscript_tables")
    args = ap.parse_args()

    outdir = Path(args.outdir)
    outdir.mkdir(parents=True, exist_ok=True)
    build_table1_dataset(Path(args.dataset), outdir)
    build_table2_protocols(outdir)
    build_table3_from_figure2(Path(args.figure2_csv), outdir)
    build_table4_baseline_main(Path(args.baseline_csv), Path(args.figure2_csv), outdir)
    build_tableS1_ablation(Path(args.ablation_dir), outdir)
    build_tableS2_shap(Path(args.shap_dir), outdir)
    build_notes(outdir)
    print(f"Done. Manuscript tables written to: {outdir}")
    for p in sorted(outdir.glob("Table*.csv")):
        print(" -", p.name)


if __name__ == "__main__":
    main()
