#!/usr/bin/env python3
"""
Build publication-grade Figure S5 for the nearest-neighbor baseline under extrapolative validation.

Input:
  knn_baseline_summary.csv generated by run_nearest_neighbor_baseline.py

Outputs:
  FigureS5_NN_baseline_publication.png
  FigureS5_NN_baseline_publication.pdf
  FigureS5_NN_baseline_publication_plot_data.csv

Design rationale:
  R² values for extrapolative NN baselines can be extremely negative. A single linear axis either
  hides the near-zero/moderately negative behaviors or visually collapses the informative pattern.
  This figure uses a broken y-axis to show both moderate failures and severe negative-R² failures
  without changing the underlying data.
"""

from __future__ import annotations

import argparse
from pathlib import Path
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

PROPERTY_ORDER = ["Density", "Refractive index", "Surface tension", "Conductivity", "Viscosity"]
BASELINE_ORDER = ["Dummy mean", "1-NN", "5-NN"]


def _baseline_label(row: pd.Series) -> str:
    baseline = str(row.get("baseline", ""))
    k = int(row.get("k", 0)) if pd.notna(row.get("k", np.nan)) else 0
    if baseline.lower().startswith("dummy"):
        return "Dummy mean"
    if baseline.upper() == "KNN" and k == 1:
        return "1-NN"
    if baseline.upper() == "KNN" and k == 5:
        return "5-NN"
    return baseline


def build_plot(input_csv: Path, outdir: Path, protocol: str = "D_leave_HBD") -> None:
    outdir.mkdir(parents=True, exist_ok=True)
    df = pd.read_csv(input_csv)

    required = {"property", "protocol", "baseline", "k", "r2_mean", "r2_sd"}
    missing = required.difference(df.columns)
    if missing:
        raise ValueError(f"Missing required columns in {input_csv}: {sorted(missing)}")

    sub = df[df["protocol"].astype(str).eq(protocol)].copy()
    if sub.empty:
        raise ValueError(f"No rows found for protocol={protocol!r}. Available: {sorted(df['protocol'].unique())}")

    sub["baseline_label"] = sub.apply(_baseline_label, axis=1)
    sub = sub[sub["property"].isin(PROPERTY_ORDER) & sub["baseline_label"].isin(BASELINE_ORDER)].copy()
    sub["property"] = pd.Categorical(sub["property"], PROPERTY_ORDER, ordered=True)
    sub["baseline_label"] = pd.Categorical(sub["baseline_label"], BASELINE_ORDER, ordered=True)
    sub = sub.sort_values(["property", "baseline_label"])

    plot_data = sub[["property", "protocol", "baseline_label", "r2_mean", "r2_sd", "mae_mean", "mae_sd", "rmse_mean", "rmse_sd"]].copy()
    plot_data.to_csv(outdir / "FigureS5_NN_baseline_publication_plot_data.csv", index=False)

    # Pivot for plotting.
    means = sub.pivot(index="property", columns="baseline_label", values="r2_mean").reindex(PROPERTY_ORDER)
    sds = sub.pivot(index="property", columns="baseline_label", values="r2_sd").reindex(PROPERTY_ORDER)

    x = np.arange(len(PROPERTY_ORDER))
    width = 0.24
    offsets = np.linspace(-width, width, len(BASELINE_ORDER))

    plt.rcParams.update({
        "font.size": 10,
        "axes.titlesize": 12,
        "axes.labelsize": 11,
        "xtick.labelsize": 10,
        "ytick.labelsize": 10,
        "legend.fontsize": 9,
        "pdf.fonttype": 42,
        "ps.fonttype": 42,
    })

    fig, (ax_top, ax_bottom) = plt.subplots(
        2, 1, sharex=True, figsize=(9.2, 5.6), gridspec_kw={"height_ratios": [2.4, 1.15], "hspace": 0.06}
    )

    # Broken-axis limits: top shows moderate values; bottom shows severe failures.
    ax_top.set_ylim(-25, 1.2)
    ax_bottom.set_ylim(-16000, -150)

    for i, label in enumerate(BASELINE_ORDER):
        y = means[label].astype(float).values
        err = sds[label].astype(float).values
        pos = x + offsets[i]
        for ax in (ax_top, ax_bottom):
            ax.bar(pos, y, width=width, label=label if ax is ax_top else None, alpha=0.9)
            ax.errorbar(pos, y, yerr=err, fmt="none", ecolor="black", elinewidth=0.9, capsize=3, zorder=3)

    for ax in (ax_top, ax_bottom):
        ax.axhline(0, linestyle="--", linewidth=1.0)
        ax.grid(axis="y", alpha=0.25)
        ax.set_xlim(-0.6, len(PROPERTY_ORDER) - 0.4)

    # Hide spines between axes and add diagonal break marks.
    ax_top.spines["bottom"].set_visible(False)
    ax_bottom.spines["top"].set_visible(False)
    ax_top.tick_params(labeltop=False, bottom=False)
    ax_bottom.tick_params(top=False)

    d = 0.008
    kwargs = dict(transform=ax_top.transAxes, color="k", clip_on=False, linewidth=0.8)
    ax_top.plot((-d, +d), (-d, +d), **kwargs)
    ax_top.plot((1 - d, 1 + d), (-d, +d), **kwargs)
    kwargs.update(transform=ax_bottom.transAxes)
    ax_bottom.plot((-d, +d), (1 - d, 1 + d), **kwargs)
    ax_bottom.plot((1 - d, 1 + d), (1 - d, 1 + d), **kwargs)

    ax_top.set_title("Nearest-neighbor baseline comparison under extrapolative validation")
    fig.text(0.04, 0.53, r"$R^2$ (mean ± SD)", va="center", rotation="vertical", fontsize=11)
    ax_bottom.set_xticks(x)
    ax_bottom.set_xticklabels(PROPERTY_ORDER, rotation=25, ha="right")

    ax_top.legend(loc="lower left", frameon=True)

    # Annotation makes the broken-axis design explicit.
    ax_bottom.text(
        0.01,
        0.06,
        "Broken y-axis preserves actual negative R² scale for severe extrapolative failures.",
        transform=ax_bottom.transAxes,
        fontsize=8.5,
        va="bottom",
        ha="left",
    )

    fig.tight_layout(rect=(0.06, 0.04, 1, 0.98))
    png = outdir / "FigureS5_NN_baseline_publication.png"
    pdf = outdir / "FigureS5_NN_baseline_publication.pdf"
    fig.savefig(png, dpi=300, bbox_inches="tight")
    fig.savefig(pdf, bbox_inches="tight")
    plt.close(fig)

    print(f"[OK] Wrote {png}")
    print(f"[OK] Wrote {pdf}")
    print(f"[OK] Wrote {outdir / 'FigureS5_NN_baseline_publication_plot_data.csv'}")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--input", default="knn_baseline_summary.csv", help="Path to knn_baseline_summary.csv")
    parser.add_argument("--outdir", default="si_final_figures", help="Output directory")
    parser.add_argument("--protocol", default="D_leave_HBD", help="Protocol to plot, default D_leave_HBD")
    args = parser.parse_args()
    build_plot(Path(args.input), Path(args.outdir), protocol=args.protocol)


if __name__ == "__main__":
    main()
